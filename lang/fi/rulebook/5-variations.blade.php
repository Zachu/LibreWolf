<h1>Vaihtoehtoiset pelitavat</h1>
<p>Tätä peliä voi muokata pelaajien taitojen ja mieltymyksen mukaisesti monilla tavoin. Alla on listattu muutamia yleisesti käytössä olevia muunnelmia sääntöihin.</p>

<h2>Lisäroolit</h2>
<p>Pelinjohtaja ei valitsekaan rooleja alkaen järjestyksessä ensimmäisestä alkaen, vaan valitsee haluamansa roolit kaikista korteista.</p>

<h2>Tuntemattomat roolit</h2>
<p>Pelinjohtaja ei kerro pelaajille käytössä olevia rooleja. Pelaajat eivät siis tiedä onko heillä esimerkiksi näkijää tai parantajaa pelissä ollenkaan.</p>
<p>Käytetään yhdessä Lisäroolit-muutoksen kanssa. Suositellaan kokeneille pelaajille.</p>

<h2>Ihmissusilauma</h2>
<p>Niin kauan kuin ihmissusia on elossa kolme tai enemmän, saavat ihmissudet öisin kaksi saalista.</p>

<h2>Pormestarin ääni ratkaisee</h2>
<p>Kyläläisten pormestarin valinta päivä-vaiheessa ei ole pakollista. Voidaan kuitenkin pelata niin, että pormestari valitaan joka aamu (tai harvemmin, tai koko pelaajan eliniäksi), ja äänestysten tasatilanteissa pormestarin ääni ratkaisee.</p>
