<h1>Aloitus</h1>
<ol>
    <li>
        <p>Pelinjohtajan valinta</p>
        <p>Joukosta valitaan pelinjohtaja. Pelinjohtaja ei osallistu peliin kummassakaan joukkueessa, vaan huolehtii pelin etenemisestä. Pelinjohtajalla olisi hyvä olla kokemusta pelaamisesta aiemmin.</p></li>
    <li>
        <p>Pelaajien asettautuminen</p>
        <p>Pelaajat asettuvat enemmän tai vähemmän rinkimäiseen asetelmaan ilman että kukaan koskettaa toista pelaajaa.</p>
    </li>
    <li>
        <p>Roolien jako</p>
        <p>Pelinjohtaja valitsee käytössä olevat roolikortit. Roolit sekoitetaan ja jaetaan, kullekin pelaajalle yksi. Pelaajat voivat katsoa roolinsa ja painaa sen mieleensä, mutta roolia ei saa missään nimessä näyttää muille pelaajille.</p>
        <p>Ensimmäisillä pelikerroilla roolit kannattaa jakaa pelaajille korttien yläkulmassa osoittamien numeroiden mukaan (esim. kahdeksalla pelaajalla kortit 1-8). Alkupään kortit ovat toiminnaltaan yksinkertaisempia ja pelin johtaminen on helpompaa. Kortit ovat myös pyritty tekemään molemmille joukkueille tasapuolisiksi.</p>
        <p>Myöhemmin pelin piristykseksi rooleja voi ottaa mukaan useampia.</p>
    </li>
</ol>

<h1>Pelin vaiheet</h1>
<p>Peli alkaa ensimmäisestä yöstä. Kun pelinjohtaja käskee tiettyjen roolien heräävän, tämän roolikortin omaavat pelaajat nostavat hiljaa päänsä ja kommunikoivat pelinjohtajalle nyökkäyksin. Pelaajien on varottava, ettei muut pelaajat huomaa heidän heränneen. Kun omat tehtävät on yön aikana tehty, käyvät pelaajat nukkumaan ja pelinjohtaja herättää tarvittaessa seuraavat pelaajat tai pelaajan.</p>

<p>Pelinjohtaja käskee kaikki pelaajat nukkumaan. Pelaajat laskevat päänsä alas, sulkevat silmänsä, eikä tästä eteenpäin yön aikana kukaan saa hiiskua sanaakaan. Poikkeuksen tekee tietysti pelinjohtaja, jonka tehtävänä on johdattaa pelaajia pelissä yön aikana.</p>

<h2>Ensimmäinen yö</h2>
<ol>
    <li>Amor herää ja ampuu nuolella kahta valitsemaansa pelaajaa, jotka tulevat rakastumaan toisiinsa.</li>
    <li>Rakastavaiset heräävät ja luovat silmäyksen toisiinsa. Tästä hetkestä eteenpäin heidän tulee pitää toistensa puolta, niin myötä- kuin vastoinkäynneissäkin.</li>
    <li>Vapaamuurarit heräävät ja tunnistavat toisensa.</li>
</ol>
<p>Tämän jälkeen peli jatkuu normaalin yön tavoin.</p>

<h2>Yö</h2>
<ol>
    <li>Meedio herää ja näkee oliko päivällä lynkattu henkilö ihmissusi.</li>
    <li>Näkijä herää ja tarkastaa onko hänen osoittama henkilö ihmissusi.</li>
    <li>Ihmissudet heräävät ja syövät osoittamansa henkilön.
    <ol>
        <li>Mikäli ihmissudet söivät Sairaan edellisellä kierroksella, yksikään susi ei saa syödä tänä yönä</li>
        <li>Mikäli pelissä on oleva Ihmissusipentu kuoli edellisenä yönä, ihmissudet saavat kostoksi syödä ylimääräisen henkilön.</li>
        <li>Mikäli pelissä on Alfasusi, Alfasusi saa oman saaliin parillisina öinä.</li>
    </ol></li>
    <li>Metsästäjä herää.
    <ol>
        <li>Mikäli metsästäjä on kuolemassa (oli kyseessä lynkkaus tai ihmissusien syönti), metsästäjä ampuu itsensä puolustamiseksi valitsemansa pelaajan. Pelinjohtaja voi nyökätä metsästäjälle kuoleman merkiksi.</li>
    </ol></li>
    <li>Noita voi herättää kerran pelissä henkiin lynkatun pelaajan ja kerran myrkyttää pelaajan.</li>
    <li>Parantaja herää ja parantaa haluamansa henkilön.</li>
</ol>

<h2>Päivä</h2>
<p>Kyläläiset heräävät, ja huomaavat keskuudestaan puuttuvan henkilöitä. Kyläläiset alkavat selvittää, mitä oikein on tapahtunut kyselemällä toisiltaan tapahtumien kulusta. Kyläläisten kannattaa valita itselleen pormestari, joka johtaa keskustelua.</p>

<p>Kyläläinen voi syyttää toista ihmissudeksi, ja mikäli tämä syytös saa kannatusta, joutuu syytetty puolustautumaan. Puolustuksen jälkeen syytetyltä voidaan kysyä lisäkysymyksiä, ja lopuksi siirrytään äänestämään. Mikäli kyläläisistä valtaosa kokee syytetyn ihmissudeksi, hänet lynkataan pois pelistä. Pormestarin ääni ratkaisee tasatilanteessa. Lynkkauksen jälkeen koittaa uusi yö. Mikäli äänestys päättyy syytetyn eduksi, ei häntä voida uudestaan syyttää samana päivänä. Tässä tilanteessa päivä jatkuu.</p>

<p>Jos kyläläisten keskuudesta ei löydy epäiltyjä, voi kyläläiset jättää äänestys- ja lynkkausvaiheen pois. Kannattaa kuitenkin huomata, että tällöin ihmissudet saavat yhden ilmaisen saalistusyön, kun kukaan heistä ei joudukaan pelistä ulos.</p>

<p>Mikäli päivä ei tunnu etenevän, voi pelinjohtaja hoputtaa pelaajia päivän loppumisella. Tällöin kyläläisiltä jää lynkkaaminen käyttämättä.</p>

<h1>Kuolema</h1>
<p>Kuolleet eivät puhu. Jos sinut syödään tai lynkataan, lakkaa puhumasta. Olet kuollut. Nuku ensimmäinen yö aivan kuten nukkuisit elävänäkin. Pelissä voi tapahtua ihmeellisiä asioita, ja kannattaa varmistaa että todellakin kuolit. Esimerkiksi Ritari pitää saattaa hengiltä kahdesti, ja Noita voi tuoda lynkatun pelaajan takaisin henkiin. Pelinjohtaja kertoo aamulla ketkä ovat kuolleet.</p>
